const maps = [];



for (let i = 0; i < mapsRaw.length; i++) {
    const mapData = mapsRaw[i];
    const devider = mapData.length == 49 ? 7 : 16
    const map = {
        player: {
            x: 0,
            y: 0
        },
        collectables: [],
        obstacles: []
    }
    maps.push(map)
    for (let index = 0; index < mapData.length; index++) {
        const element = mapData[index];
        let x = Math.floor(index / devider);
        let y = index - x * devider;

        if (element == 39) {
            map.obstacles.push({
                x: x * 32,
                y: y * 32,
                item: Grid.TREE
            })
        } else if (element == 121) {
            map.collectables.push({
                x: x * 32,
                y: y * 32,
                item: Grid.HEALTHPOTION
            })
        } else if (element == 132) {
            map.player.x = x * 32;
            map.player.y = y * 32;
        }
    }
}

